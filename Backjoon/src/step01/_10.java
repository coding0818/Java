public class _10 {
	public static void main(String[] args) {
		
		Scanner sc = new Scanner(System.in);
		
		int k = sc.nextInt();
		int q = sc.nextInt();
		int l = sc.nextInt();
		int b = sc.nextInt();
		int n = sc.nextInt();
		int p = sc.nextInt();
		
		if(k >= 0 && k <= 10);
		if(q >= 0 && q <= 10);
		if(l >= 0 && l <= 10);
		if(b >= 0 && b <= 10);
		if(n >= 0 && n <= 10);
		if(p >= 0 && p <= 10);
		 
	
		System.out.print(1 - k+" ");
		System.out.print(1 - q+" ");
		System.out.print(2 - l+" ");
		System.out.print(2 - b+" ");
		System.out.print(2 - n+" ");
		System.out.print(8 - p+" ");
	}

}
